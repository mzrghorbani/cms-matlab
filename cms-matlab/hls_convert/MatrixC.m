function [ C ] = MatrixC(c00, c11, c22, c33, c01, c23)
        
    C = zeros(4);
    C(1,1) = c00;
    C(2,2) = c11;
    C(3,3) = c22;
    C(4,4) = c33;
    C(1,2) = c01;
    C(3,4) = c23;
    C(1,3) = 0;
    C(1,4) = 0;
    C(2,3) = 0;
    C(2,4) = 0;
    C(2,1) = C(1,2);
    C(4,3) = C(3,4);
    C(3,1) = C(1,3);
    C(4,1) = C(1,4);
    C(3,2) = C(2,3);
    C(4,2) = C(2,4);
    
end


